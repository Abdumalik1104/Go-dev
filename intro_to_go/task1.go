// Exercise 1: Hello, World!

package main

import "fmt"

func main() {
	fmt.Println("Hello, World!")
}

// Command to compile and run Go program:
// To run: go run main.go
// To compile: go build main.go

// Basic components of a Go program:
// Package: Declares the program's package, e.g., package main.
// Import: Brings in libraries, e.g., import "fmt".
// Function: Defines logic, e.g., func main() is the entry point.

// How Go handles package imports:
// Go uses the import keyword to include standard and third-party libraries.
// Unused imports cause errors, and dependencies are managed with go.mod for third-party packages.
